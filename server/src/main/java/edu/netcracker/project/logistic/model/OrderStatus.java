package edu.netcracker.project.logistic.model;

public class OrderStatus {
    private Long id;
    private String name;

  public    OrderStatus() {

    }

    public OrderStatus(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
